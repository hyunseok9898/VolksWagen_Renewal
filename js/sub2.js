var customIcon = L.icon({
  iconUrl: 'img/sub2/marker.svg',
  iconSize: [36, 40],
  iconAnchor: [18, 40],
  tooltipAnchor: [0, -42],
});

var map = L.map('map').setView([37.5665, 126.9780], 12);
L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
  attribution: '&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors',
  maxZoom: 19,
}).addTo(map);

var currentMarkers = [];
var modal = document.getElementById("myModal");

function createMarkerContent(place) {
  return (
    '<div class="textWrap">' +
    '<h4>' + place.place_name + '</h4>' +
    '<div class="desc">' +
    '<p><span>주소</span>' + place.address_name + '</p>' +
    '<p><span>전화번호</span>' + place.phone + '</p>' +
    '<p><span>장소 상세</span><a class="place_link" href="' + place.place_url + '" target="_blank">' + place.place_url + '</a></p>' +
    '</div>' +
    '</div>'
  );
}

function openmodal(place) {
  document.getElementById('modal_content').innerHTML = createMarkerContent(place);
  modal.style.display = 'block';
}

function hideModal() {
  modal.style.display = 'none';
}

function closeModalBtn() {
  document.getElementsByClassName('modalCloseBtn')[0].onclick = function() {
    hideModal();
  };
}

function clearMarkers() {
  currentMarkers.forEach(function(marker) { map.removeLayer(marker); });
  currentMarkers = [];
}

function displayPlaces(places) {
  clearMarkers();
  var placesList = document.getElementById('placesList');
  placesList.innerHTML = '';

  if (!places || places.length === 0) {
    document.getElementById('place_count').innerHTML = '0개';
    return;
  }

  var bounds = L.latLngBounds();

  for (var i = 0; i < places.length; i++) {
    (function(place) {
      var marker = L.marker([place.lat, place.lng], { icon: customIcon }).addTo(map);
      marker.bindTooltip(
        '<div class="customOverlay"><p>' + place.place_name + '</p></div>',
        { direction: 'top', className: 'customWrap', permanent: false }
      );
      marker.on('click', function() {
        openmodal(place);
        closeModalBtn();
      });
      currentMarkers.push(marker);
      bounds.extend([place.lat, place.lng]);

      var li = document.createElement('li');
      var para = document.createElement('p');
      para.textContent = place.place_name;
      li.placeData = place;
      li.appendChild(para);
      placesList.appendChild(li);

      li.addEventListener('mouseover', function() { marker.openTooltip(); });
      li.addEventListener('mouseout', function() { marker.closeTooltip(); });
      li.addEventListener('click', (function(p) {
        return function() { openmodal(p); closeModalBtn(); };
      })(place));
    })(places[i]);
  }

  map.fitBounds(bounds, { padding: [50, 50] });
  document.getElementById('place_count').innerHTML = places.length + '개';
  addDetailDivOnSmallScreen();
}

function searchByLocation() {
  hideModal();
  var selectedLocation = document.getElementById('netsido').value;
  displayPlaces(serviceCenterData[selectedLocation] || []);
}

