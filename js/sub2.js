function addDetailDivOnSmallScreen() {
  var placesListLi = document.querySelectorAll('#placesList li');

  placesListLi.forEach(function(item) {
    var existingDetailDiv = item.querySelector('.detail');
    var placeData = item.placeData;

    if (window.innerWidth < 768) {
      if (!existingDetailDiv) {
        var detailDiv = document.createElement('div');
        var mapContainerId = 'map_container_' + Math.random().toString(36).substr(2, 9);
        var mapContainerEl = document.createElement('div');
        mapContainerEl.id = mapContainerId;
        mapContainerEl.style.height = '200px';
        detailDiv.className = 'detail';
        item.appendChild(detailDiv);
        detailDiv.appendChild(mapContainerEl);
        detailDiv.innerHTML += createMarkerContent(placeData);

        var staticMapInstance = null;
        item.addEventListener('click', function() {
          if (this.classList.contains('open')) {
            this.classList.remove('open');
          } else {
            closeAllLi();
            this.classList.add('open');
            if (!staticMapInstance) {
              var el = document.getElementById(mapContainerId);
              if (el) {
                staticMapInstance = L.map(el, { zoomControl: false, dragging: false, scrollWheelZoom: false }).setView([placeData.lat, placeData.lng], 15);
                L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png').addTo(staticMapInstance);
                L.marker([placeData.lat, placeData.lng], { icon: customIcon }).addTo(staticMapInstance);
              }
            }
          }
        });
      }
    } else if (existingDetailDiv) {
      item.removeChild(existingDetailDiv);
    }
  });
}

function closeAllLi() {
  document.querySelectorAll('#placesList li').forEach(function(li) {
    li.classList.remove('open');
  });
}

window.addEventListener('load', addDetailDivOnSmallScreen);
window.addEventListener('resize', addDetailDivOnSmallScreen);
